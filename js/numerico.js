function timer() {
    var days = Math.floor(seconds / 24 / 60 / 60);
    var hoursLeft = Math.floor((seconds) - (days * 86400));
    var hours = Math.floor(hoursLeft / 3600);
    var minutesLeft = Math.floor((hoursLeft) - (hours * 3600));
    var minutes = Math.floor(minutesLeft / 60);
    var remainingSeconds = seconds % 60;
    if (remainingSeconds < 10) {
        remainingSeconds = "0" + remainingSeconds;
    }
    if (minutes < 10) {
        minutes = "0" + minutes;
    }
    document.getElementById('timer').innerHTML = "";

    document.getElementById('timer').innerHTML = ' <strong> Tiempo: ' + hours + ":" + minutes + ":" + remainingSeconds + '</strong>';

    // if (seconds == 5390) {
    //     alert('En 10 minutos terminar√° el examen');
    // }

    if (seconds === 0) {
        clearInterval(countdownTimer);
        alert('El examen ha terminado!');
        window.location.href = urlmegapro + "/site/resultados.html";
    } else {
        seconds--;
    }
}

function getRandoms(size) {
    const numbers = [];
    for (let index = 0; index < size; index++) {
        let number = getRandomArbitrary(0, 70);
        while (numbers.includes(number)) {
            number = getRandomArbitrary(0, 70);
        }
        numbers.push(number);
    }
    return numbers;
}

function getRandomArbitrary(min, max) {
    return Math.trunc(Math.random() * (max - min) + min);
}

